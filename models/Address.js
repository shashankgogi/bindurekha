class Address {
  constructor(
    id,
    name,
    mobileNumber,
    pincode,
    address,
    landmark,
    city,
    state,
    country,
    locality
  ) {
    this.id = id;
    this.name = name;
    this.mobileNumber = mobileNumber;
    this.pincode = pincode;
    this.address = address;
    this.landmark = landmark;
    this.city = city;
    this.state = state;
    this.country = country;
    this.locality = locality;
  }
}

export default Address;
